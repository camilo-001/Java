import javax.swing.*;

public class ForExample {

    public static void main(String[] args) {

        //ciclo for
        // for (inicialización; Condicion ; Iteración)


        for (int numero = 1; numero < 101; numero++) {

            if (numero % 2 == 0) {

                System.out.println(numero);
            }

        }


    }

}